package ss01_introduction_to_java.thuc_hanh;

public class DeclareVariable {
    public static void main(String[] args) {
        int i=10;  // khai báo biến i là kiểu int có giá trị là 10
        float f = 20.5f; // Khai báo biến f có kiểu là float có giá trị là 20.5
        double d = 20.5; // Khai báo biến d có kiểu là double có giá trị là 20.5
        boolean b = true; // Khai báo biến b là kiểu boolean có giá trị là true
        char c = 'c'; // Khai báo biến c có kiểu là char có giá trị là kí tự a
        String s = "Huy"; //Khai báo biến s có kiểu là String có giá trị là chuỗi Hà Nội

        System.out.println("The result i = " + i);
        System.out.println("The result f = " + f);
        System.out.println("The result d = " + d);
        System.out.println("The result b = " + b);
        System.out.println("The result c = " + c);
        System.out.println("The result s = " + s);
    }
}
